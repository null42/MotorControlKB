//计算电机速度
void Motor_Speed_Get(void)
{
	encoder_count = TIM3->CNT;		//当前CNT的值+溢出次数*定时器的脉冲触发数
	
//	Motor.speed_M_rpm = (float)(encoder_count - encoder_count_last) * 3.66211f;					//电机机械角速度 speed = encoder_count_err/(编码器线数*4)/(PWMZD_Count/TIM1_Frq)*60
	if(pi_spd.Ref >= 0)
	{
		if(encoder_count < encoder_count_last)
			Motor.speed_M_rpm = (float)(encoder_count + TIM3_Pulse - encoder_count_last)*Encoder_Fre/Encoder_Pulse;
		else
			Motor.speed_M_rpm = (float)(encoder_count - encoder_count_last)*Encoder_Fre/Encoder_Pulse;		
	}
	else
	{
		if(encoder_count > encoder_count_last)
			Motor.speed_M_rpm = (float)(encoder_count - (encoder_count_last + TIM3_Pulse))*Encoder_Fre/Encoder_Pulse;
		else
			Motor.speed_M_rpm = (float)(encoder_count - encoder_count_last)*Encoder_Fre/Encoder_Pulse;
	}
	
	Motor.speed_E_rpm = Motor.speed_M_rpm * Motor.P;																//电角速度 = 机械角速度 * 极对数
	encoder_count_last = encoder_count;
}

//计算电机电角度
void Motor_Angle_Get(void)
{					
	Motor.E_theta = (float)TIM3->CNT/Encoder_Pulse*PIX2*Motor.P;										//CNT/16384(编码器转一圈计数值)*2PI*极对数
	
	while(Motor.E_theta > PIX2)
		Motor.E_theta -= PIX2;
}

